function [result] = getBasicInfor(bubble)

    result.x2dGauche = bubble.x2dGauche;
    result.x2dDroit = bubble.x2dDroit;
    result.y2dHaut = bubble.y2dHaut;
    result.y2dBas = bubble.y2dBas;
    result.n1 = bubble.n1;
    result.n2 = bubble.n2;
    result.Ra = bubble.Ra;
    result.Ro = bubble.Ro;
    result.Pr = bubble.Pr;
    result.St = bubble.St;
    result.Ff = bubble.Ff;
    result.Delta = bubble.Delta;
    result.maxN  = bubble.maxN;

end